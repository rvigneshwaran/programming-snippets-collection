#Hello World
print("Hello World")

#Normal String Printing
firstStringVariable <- "Hello World"
print(firstStringVariable)

#Boolean Instance
booleanVariable <- TRUE
print(class(booleanVariable)) #prints Class of The Object
print(booleanVariable) #prints value of The Object

#Numeric Instance
numericVariable <- 23.5
print(class(numericVariable)) #prints Class of The Object
print(numericVariable) #prints value of The Object

#Integer Instance
integerVariable <- 23L
print(class(integerVariable)) #prints Class of The Object
print(integerVariable) #prints value of The Object

#Complex Instance
complexVariable <- 3+2i
print(class(complexVariable)) #prints Class of The Object
print(complexVariable) #prints value of The Object

#Character Instance
characterInstance <- "TRUE"
print(class(characterInstance)) #prints Class of The Object
print(characterInstance) #prints value of The Object

